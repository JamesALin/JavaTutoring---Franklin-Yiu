class Main {
  public static void main(String[] args) {
    System.out.print("The difference between Println() and Print() is that Println() will push the cursor to the next line whereas Print() will print the next strings on the same line.");
    System.out.print(" This string is printed next to the last string because Print() does not skip to the next line. ");
    System.out.println("However, these next strings will be on different lines");
    System.out.println("because we used Println()");
  }
}